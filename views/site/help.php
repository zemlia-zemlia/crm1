<p>1) Пополните баланс на произвольную сумму через платежную систему. Пополнить можно с любой карты или с кошелька Яндекс Деньги.</p>

<img src="../web/images/help/5.png">
<p>2) После пополнения счета - Перейдите в раздел - Продлить подписку.</p>
<img src="../web/images/help/6.png">

<p>3) Выберите вариант подписки, который вас интересует.</p>
